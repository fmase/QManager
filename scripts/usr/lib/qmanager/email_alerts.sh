#!/bin/sh
# =============================================================================
# email_alerts.sh — Email Alert Library for QManager
# =============================================================================
# Sourced by qmanager_poller and CGI scripts. Detects prolonged internet
# downtime and sends a recovery email (with downtime details) via msmtp
# once connectivity is restored.
#
# Dependencies: jq, msmtp, qlog_* functions (optional)
# Install location: /usr/lib/qmanager/email_alerts.sh
#
# Global variables used from poller:
#   Read: conn_internet_available ("true"/"false"/"null")
#
# Config:  /etc/qmanager/email_alerts.json
# Log:     /tmp/qmanager_email_log.json (NDJSON, max 100 entries)
# msmtp:   /etc/qmanager/msmtprc (generated by CGI on save)
# Reload:  /tmp/qmanager_email_reload (flag file, touched by CGI)
# =============================================================================

[ -n "$_EMAIL_ALERTS_LOADED" ] && return 0
_EMAIL_ALERTS_LOADED=1

# --- Constants ---------------------------------------------------------------
_EA_CONFIG="/etc/qmanager/email_alerts.json"
_EA_MSMTP_CONFIG="/etc/qmanager/msmtprc"
_EA_LOG_FILE="/tmp/qmanager_email_log.json"
_EA_RELOAD_FLAG="/tmp/qmanager_email_reload"
_EA_MAX_LOG=100

# --- State (populated by email_alerts_init / _ea_read_config) ----------------
_ea_enabled="false"
_ea_sender=""
_ea_recipient=""
_ea_app_password=""
_ea_threshold_minutes=5

# --- Downtime tracking (poller runtime only) ---------------------------------
_ea_was_down="false"
_ea_downtime_start=0

# =============================================================================
# _ea_read_config — Read settings from config JSON
# =============================================================================
_ea_read_config() {
    if [ ! -f "$_EA_CONFIG" ]; then
        _ea_enabled="false"
        return 1
    fi

    _ea_enabled=$(jq -r '(.enabled) | if . == null then "false" else tostring end' "$_EA_CONFIG" 2>/dev/null)
    _ea_sender=$(jq -r '.sender_email // ""' "$_EA_CONFIG" 2>/dev/null)
    _ea_recipient=$(jq -r '.recipient_email // ""' "$_EA_CONFIG" 2>/dev/null)
    _ea_app_password=$(jq -r '.app_password // ""' "$_EA_CONFIG" 2>/dev/null)
    _ea_threshold_minutes=$(jq -r '.threshold_minutes // 5' "$_EA_CONFIG" 2>/dev/null)

    # Validate
    if [ "$_ea_enabled" != "true" ]; then
        _ea_enabled="false"
        return 0
    fi
    if [ -z "$_ea_sender" ] || [ -z "$_ea_recipient" ] || [ -z "$_ea_app_password" ]; then
        _ea_enabled="false"
        return 1
    fi
    return 0
}

# =============================================================================
# email_alerts_init — Called once at poller startup
# =============================================================================
email_alerts_init() {
    _ea_read_config
    if [ "$_ea_enabled" = "true" ]; then
        qlog_info "Email alerts enabled: sender=$_ea_sender recipient=$_ea_recipient threshold=${_ea_threshold_minutes}m"
    else
        qlog_info "Email alerts disabled or not configured"
    fi
}

# =============================================================================
# check_email_alert — Called every poll cycle after detect_events
# =============================================================================
# Reads conn_internet_available (global from poller).
# Tracks downtime start, sends recovery email when internet returns after
# a downtime that exceeded the configured threshold.
# =============================================================================
check_email_alert() {
    # Check for reload flag (CGI saved new settings)
    if [ -f "$_EA_RELOAD_FLAG" ]; then
        rm -f "$_EA_RELOAD_FLAG"
        _ea_read_config
        qlog_info "Email alerts config reloaded: enabled=$_ea_enabled"
    fi

    # Skip if disabled or not fully configured
    [ "$_ea_enabled" != "true" ] && return 0

    # Skip null/stale ping state
    [ "$conn_internet_available" = "null" ] && return 0
    [ -z "$conn_internet_available" ] && return 0

    if [ "$conn_internet_available" = "false" ]; then
        # Internet is down — record start time if not already tracking
        if [ "$_ea_was_down" != "true" ]; then
            _ea_downtime_start=$(date +%s)
            _ea_was_down="true"
            qlog_debug "Email alerts: downtime tracking started at $_ea_downtime_start"
        fi

    elif [ "$conn_internet_available" = "true" ] && [ "$_ea_was_down" = "true" ]; then
        # Internet recovered — check if downtime exceeded threshold
        local now duration threshold_secs
        now=$(date +%s)
        duration=$((now - _ea_downtime_start))
        threshold_secs=$((_ea_threshold_minutes * 60))

        if [ "$duration" -ge "$threshold_secs" ]; then
            qlog_info "Email alerts: sending recovery email (downtime=${duration}s, threshold=${threshold_secs}s)"
            _ea_send_recovery_email "$_ea_downtime_start" "$duration"
        else
            qlog_debug "Email alerts: downtime ${duration}s < threshold ${threshold_secs}s, no email"
        fi

        # Reset tracking
        _ea_was_down="false"
        _ea_downtime_start=0
    fi
}

# =============================================================================
# _ea_send_recovery_email — Format and send recovery notification
# =============================================================================
_ea_send_recovery_email() {
    local start_epoch="$1"
    local duration_secs="$2"

    # Format start time
    # BusyBox date supports -d @epoch on most builds; fall back to awk strftime
    # (date -r is macOS/FreeBSD only — not available on BusyBox)
    local start_time
    start_time=$(date -d "@$start_epoch" "+%Y-%m-%d %H:%M:%S" 2>/dev/null) || \
        start_time=$(awk "BEGIN{print strftime(\"%Y-%m-%d %H:%M:%S\",$start_epoch)}" 2>/dev/null) || \
        start_time="$start_epoch"

    # Format duration
    local dur_text
    dur_text=$(_ea_format_duration "$duration_secs")

    # Build HTML email
    local html
    html=$(_ea_build_recovery_html "$start_time" "$dur_text" "$_ea_threshold_minutes")

    # Send
    local trigger_text="Connection recovered (down ${dur_text})"
    if _ea_do_send "Connection Recovered" "$html"; then
        _ea_log_event "$trigger_text" "sent" "$_ea_recipient"
    else
        _ea_log_event "$trigger_text" "failed" "$_ea_recipient"
    fi
}

# =============================================================================
# _ea_send_test_email — Send a test email (called by CGI)
# =============================================================================
_ea_send_test_email() {
    local html
    html=$(_ea_build_test_html)

    if _ea_do_send "Test Alert" "$html"; then
        _ea_log_event "Test email" "sent" "$_ea_recipient"
        return 0
    else
        _ea_log_event "Test email" "failed" "$_ea_recipient"
        return 1
    fi
}

# =============================================================================
# _ea_do_send — Core msmtp send function
# =============================================================================
_ea_do_send() {
    local subject="$1"
    local html_body="$2"

    if ! command -v msmtp >/dev/null 2>&1; then
        qlog_error "Email alerts: msmtp not installed"
        return 1
    fi

    if [ ! -f "$_EA_MSMTP_CONFIG" ]; then
        qlog_error "Email alerts: msmtp config not found at $_EA_MSMTP_CONFIG"
        return 1
    fi

    {
        printf "From: %s\r\n" "$_ea_sender"
        printf "To: %s\r\n" "$_ea_recipient"
        printf "Subject: [QManager] %s\r\n" "$subject"
        printf "MIME-Version: 1.0\r\n"
        printf "Content-Type: text/html; charset=UTF-8\r\n"
        printf "\r\n"
        printf "%s" "$html_body"
    } | msmtp -C "$_EA_MSMTP_CONFIG" "$_ea_recipient" 2>/dev/null

    local rc=$?
    if [ $rc -eq 0 ]; then
        qlog_info "Email alerts: email sent successfully to $_ea_recipient"
    else
        qlog_error "Email alerts: msmtp failed with exit code $rc"
    fi
    return $rc
}

# =============================================================================
# _ea_log_event — Append entry to NDJSON log file
# =============================================================================
_ea_log_event() {
    local trigger="$1" status="$2" recipient="$3"
    local ts
    ts=$(date "+%Y-%m-%d %H:%M:%S")

    jq -n -c \
        --arg ts "$ts" \
        --arg trigger "$trigger" \
        --arg status "$status" \
        --arg recipient "$recipient" \
        '{timestamp: $ts, trigger: $trigger, status: $status, recipient: $recipient}' \
        >> "$_EA_LOG_FILE"

    # Trim to max entries
    local count
    count=$(wc -l < "$_EA_LOG_FILE" 2>/dev/null || echo 0)
    if [ "$count" -gt "$_EA_MAX_LOG" ]; then
        local tmp="${_EA_LOG_FILE}.tmp"
        if tail -n "$_EA_MAX_LOG" "$_EA_LOG_FILE" > "$tmp" 2>/dev/null; then
            mv "$tmp" "$_EA_LOG_FILE" 2>/dev/null || rm -f "$tmp"
        else
            rm -f "$tmp"
        fi
    fi
}

# =============================================================================
# _ea_format_duration — Convert seconds to human-readable string
# =============================================================================
_ea_format_duration() {
    local secs="$1"
    local hours mins remaining

    hours=$((secs / 3600))
    remaining=$((secs % 3600))
    mins=$((remaining / 60))
    remaining=$((remaining % 60))

    if [ "$hours" -gt 0 ]; then
        printf "%dh %dm %ds" "$hours" "$mins" "$remaining"
    elif [ "$mins" -gt 0 ]; then
        printf "%dm %ds" "$mins" "$remaining"
    else
        printf "%ds" "$remaining"
    fi
}

